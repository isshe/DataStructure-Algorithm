#include <stdio.h>
#include <stdlib.h>


void merge(int *a, int *b, int lo, int mid, int hi)
{
     int    i = lo;
     int    j = mid + 1;
     int    k = 0;

     //copy a[lo..hi] to b[lo..hi].
     for (k = lo; k <= hi; k++)
     {
         b[k] = a[k];
     }

     for (k = lo; k <= hi; k++)
     {
          if (i > mid)          //到达边缘
          {
              a[k] = b[j];
              j++;
          }
          else if (j > hi)    //到达边缘
          {
              a[k] = b[i];
              i++;
          }
          else if (b[i] > b[j])
          {
               a[k] = b[j];
               j++;
          }
          else          //b[i] < b[j]
          {
               a[k] = b[i];
               i++;
          }
     }
}
